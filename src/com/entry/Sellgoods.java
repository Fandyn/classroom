package com.entry;


public class Sellgoods {

  private long sid;
  private String sname;
  private long snumber;
  private String snorms;
  private double sprice;
  private long rid;


  public long getSid() {
    return sid;
  }

  public void setSid(long sid) {
    this.sid = sid;
  }


  public String getSname() {
    return sname;
  }

  public void setSname(String sname) {
    this.sname = sname;
  }


  public long getSnumber() {
    return snumber;
  }

  public void setSnumber(long snumber) {
    this.snumber = snumber;
  }


  public String getSnorms() {
    return snorms;
  }

  public void setSnorms(String snorms) {
    this.snorms = snorms;
  }


  public double getSprice() {
    return sprice;
  }

  public void setSprice(double sprice) {
    this.sprice = sprice;
  }


  public long getRid() {
    return rid;
  }

  public void setRid(long rid) {
    this.rid = rid;
  }

}
